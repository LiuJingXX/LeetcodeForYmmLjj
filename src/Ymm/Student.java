package Ymm;

public class Student {
    int age;
    String name;
    int id;
    public int getAge() {
        return age;
    }
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
